class FastImageInterpreter(pdfminer.pdfinterp.PDFPageInterpreter):
    """Experimental, think about the sence of this ?optimization?."""

    # TODO: SEE DOCSTRING

    def __init__(self, rsrcmgr, device):
        super().__init__(rsrcmgr, device)
        self.fast = {
            'CS': self.do_CS,
            'Do': self.do_Do,
            'EI': self.do_EI,
            'MP': self.do_MP,
            'Q': self.do_Q,
            'SC': self.do_SC,
            'SCN': self.do_SCN,
            'cm': self.do_cm,
            'cs': self.do_cs,
            'sc': self.do_sc,
            'scn': self.do_scn,
        }

    # pylint:disable=W0613,R0201
    def render_char(self, matrix, font, fontsize, scaling, rise, cid, ncs,
                    graphicstate):
        # assert 0
        return

    def render_string(self, textstate, seq, ncs, graphicstate):
        return

    def do_TJ(self, seq):
        return

    def execute(self, streams):  # pylint:disable=R1260
        try:
            parser = pdfminer.pdfinterp.PDFContentParser(streams)
        except pdfminer.psparser.PSEOF:
            # empty page
            return
        while 1:
            try:
                (_, obj) = parser.nextobject()
            except pdfminer.psparser.PSEOF:
                break
            if isinstance(obj, pdfminer.psparser.PSKeyword):
                name = pdfminer.psparser.keyword_name(obj)
                # print(name)
                try:
                    func = self.fast[name]
                except KeyError:
                    continue
                nargs = func.__code__.co_argcount - 1
                # nargs = six.get_function_code(func).co_argcount - 1
                if nargs:
                    args = self.pop(nargs)
                    if len(args) == nargs:
                        func(*args)
                else:
                    func()

            else:
                self.push(obj)


def create_fastimageextractor(imagelistener):
    rsrcmgr = pdfminer.pdfinterp.PDFResourceManager()
    device = ImageConverter(rsrcmgr, imagewriter=imagelistener)
    # interpreter = FastImageInterpreter(rsrcmgr, device)
    interpreter = pdfminer.pdfinterp.PDFPageInterpreter(rsrcmgr, device)
    return interpreter
